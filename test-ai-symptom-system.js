// Test individual symptom processing
async function testSymptomProcessing() {
  console.log('üîç Testing AI Symptom Processing...\n');
  
  for (let i = 0; i < testCases.length; i++) {
    const testCase = testCases[i];
    console.log(`üìã Test ${i + 1}: ${testCase.name}`);
    console.log(`   Symptoms: "${testCase.symptoms}"`);
    console.log(`   Language: ${testCase.language}`);
    console.log(`   Expected: ${testCase.expectedType}`);
    
    try {
      const startTime = Date.now();
      const result = await SymptomService.processSymptoms(testCase.symptoms, testCase.language);
      const processingTime = Date.now() - startTime;
      
      console.log(`   ‚úÖ Result Type: ${result.type}`);
      console.log(`   ‚è±Ô∏è Processing Time: ${processingTime}ms`);
      console.log(`   üìù Response Length: ${result.message?.length || 0} characters`);
      
      // Check if result type matches expectation
      const typeMatch = result.type === testCase.expectedType;
      console.log(`   üéØ Type Match: ${typeMatch ? 'CORRECT' : 'MISMATCH'}`);
      
      // Show response preview
      if (result.message) {
        const preview = result.message.substring(0, 200) + (result.message.length > 200 ? '...' : '');
        console.log(`   üí¨ Response Preview: "${preview}"`);
      }
      
      // Log additional details
      if (result.confidence) {
        console.log(`   üìä Confidence: ${(result.confidence * 100).toFixed(1)}%`);
      }
      
      if (result.severity) {
        console.log(`   ‚ö†Ô∏è Severity: ${result.severity}`);
      }
      
      testResults.push({
        test: testCase.name,
        status: 'PASS',
        typeCorrect: typeMatch,
        processingTime: processingTime,
        responseGenerated: !!result.message
      });
      
    } catch (error) {
      console.log(`   ‚ùå Test Failed: ${error.message}`);
      testResults.push({
        test: testCase.name,
        status: 'FAIL',
        error: error.message
      });
    }
    
    console.log(''); // Empty line for readability
  }
}

